// Basic I/O using cin and cout

#include <iostream>

using namespace std;

int main() 
{
  cout << "Hello World" << endl;

  cout << "Hello";
  cout << "World" << endl;

  cout << "Hello World" << endl;
  cout << "Hello" << " world!" << endl;
  cout << "Hello" << " world!\n";
  cout << endl;
  cout << "Hello\nOut\nThere\n";

  return 0;
}